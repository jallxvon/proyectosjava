package Get_set;
public class Jugadores 
{
    private String pasaporte, nombre, apellido, alergia, tipo_s, posicion;
    private int cod_pais, peso;
	public String getPasaporte() {
		return pasaporte;
	}
	public void setPasaporte(String pasaporte) {
		this.pasaporte = pasaporte;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	public String getAlergia() {
		return alergia;
	}
	public void setAlergia(String alergia) {
		this.alergia = alergia;
	}
	public String getTipo_s() {
		return tipo_s;
	}
	public void setTipo_s(String tipo_s) {
		this.tipo_s = tipo_s;
	}
	public String getPosicion() {
		return posicion;
	}
	public void setPosicion(String posicion) {
		this.posicion = posicion;
	}
	public int getCod_pais() {
		return cod_pais;
	}
	public void setCod_pais(int cod_pais) {
		this.cod_pais = cod_pais;
	}
	public int getPeso() {
		return peso;
	}
	public void setPeso(int peso) {
		this.peso = peso;
	}
    
    
}
