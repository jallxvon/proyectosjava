package Get_set;

public class Comida 
{
	private String cod_menu;
	private String detalle;
	private int consumidos;
	private int total;
	
	
	public String getCod_menu() {
		return cod_menu;
	}
	public void setCod_menu(String cod_menu) {
		this.cod_menu = cod_menu;
	}
	public String getDetalle() {
		return detalle;
	}
	public void setDetalle(String detalle) {
		this.detalle = detalle;
	}
	public int getConsumidos() {
		return consumidos;
	}
	public void setConsumidos(int consumidos) {
		this.consumidos = consumidos;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	
	
	
	
	
}
